public abstract class Pessoa
{
  public abstract void dizerAlgo(string algo);
  public abstract int multiplicar(int i, int j);
  public abstract int somar(int i, int j);
  public abstract int subtrair(int i, int j);
  public abstract int dividir(int i, int j);
  public abstract int calcularRestoDeDivisao(int i, int j);
}

